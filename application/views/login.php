<div id="inner_chap_content_1"><p>Người đăng: Giấy Trắng</p>
<p>Ầm ầm! </p>
<p>Mật thất đại môn ầm vang sụp đổ, nện trên sàn nhà.</p>
<p>Tần Giác uống một hớp rượu, đi vào mật thất.</p>
<p>Ông! </p>
<p>Dường như kích hoạt lên một loại nào đó trận pháp, chung quanh đột nhiên sáng
lên tia sáng chói mắt, chiếu xạ trên người Tần Giác.</p>
<p>Đôm đốp! </p>
<p>Những ánh sáng này bên trong ẩn chứa cực kỳ đáng sợ lực hủy diệt, cho dù là
Chí Tôn cảnh cường giả vậy khó có thể chịu đựng, nhưng mà Tần Giác lại chỉ là
hé mắt, thậm chí liền y phục đều không có phá.</p>
<p>"Người đều đã chết, còn làm nhiều như vậy loè loẹt đồ vật ." </p>
<p>Nhếch miệng, Tần Giác thân bên trên lập tức tách ra càng thêm loá mắt kim
quang, trong nháy mắt đem trận pháp phá hủy.</p>
<p>Không giống với bên ngoài thật dài sách khung, trong mật thất công pháp đều là
đơn độc đặt ở ngọc cách bên trong, còn có thật nhiều cao giai Linh khí cùng
đan dược.</p>
<p>Linh Đế trước khi chết liền từng tế ra qua một kiện Linh khí, đáng tiếc tại
hoàng kim Gatling điên cuồng công kích hạ chỉ giữ vững được mấy chục giây tả
hữu liền bị xé nát.</p>
<p>Nghiêm ngặt trên ý nghĩa mà nói, hoàng kim Gatling hẳn là đạt đến Thánh khí
cấp bậc, với lại tại Tần Giác trong tay uy lực còn hội tăng cường mấy lần, nếu
không vậy không có khả năng giết chết Linh Đế.</p>
<p>"( Thiên Tượng Trấn Ngục Kính ), cái này nhìn rất không tệ ." </p>
<p>"Truyền kỳ Linh khí, Huyền Trọng Xích ." </p>
<p>"( Nguyên Chi Không ), đây là công pháp gì?" </p>
<p>"..." </p>
<p>Tần Giác hơi nhìn xuống, Linh tộc không hổ là mười hai Thánh tộc một trong,
cho dù vài ngàn năm trước mới đi đến Linh Ương giới, nhưng lại nội tình sâu
dầy vô cùng.</p>
<p>Các loại công pháp cái gì cần có đều có, Linh khí vậy tất cả đều là Chí tôn
cấp trở lên, chừng trên trăm kiện nhiều.</p>
<p>So sánh dưới, chi Tiền Vô Cực bí cảnh đơn giản cùng bãi rác không có gì khác
biệt.</p>
<p>Đáng tiếc duy nhất là, Tần Giác cũng không nhìn thấy có quan hệ với hóa thân
công pháp, xem ra chỉ có thể đi tám đại thánh địa đi dạo.</p>
<p>Nghĩ tới đây, Tần Giác vung tay lên, trực tiếp thanh tất cả mọi thứ đều thu
nhập nhẫn trữ vật, dù sao hiện tại Linh tộc đã biến mất, nhiều như vậy công
pháp Linh khí còn không bằng toàn bộ mang về Huyền Ất Sơn.</p>
<p>Lúc đầu Tần Giác chỉ là muốn đến Trung Châu thánh địa du lịch, ai có thể nghĩ
tới sẽ dính dấp ra kinh người như thế bí mật, nếu không có Tần Giác trùng hợp
phát hiện, chỉ sợ mười ngày sau Tu La giới liền hội đại quân áp cảnh, đồng
thời lấy Linh tộc làm căn cơ, đối Trung Châu thánh địa tạo thành hủy diệt tính
đả kích.</p>
<p>Mà một khi Trung Châu thánh địa luân hãm, như vậy còn lại bốn cái hoàn cảnh
vậy bất quá là đợi làm thịt cừu non thôi, tùy tiện phái mấy vị Thánh cảnh hoặc
là Thánh Vương liền có thể quét ngang.</p>
<p>Làm xong cái này chút, Tần Giác lại thanh Linh tộc địa phương khác vơ vét một
bản, dĩ vãng hắn đều không có giết người liếm hộp thói quen, nhưng lần này lại
cải biến chú ý.</p>
<p>Ngoài dự liệu là, Linh tộc có một cái dị thường to lớn hầm rượu, bên trong
không chỉ có chứa đựng rất nhiều trăm năm linh tửu, thậm chí còn có không ít
ngàn năm linh tửu.</p>
<p>Đối Tần Giác tới nói, đây không thể nghi ngờ là lớn nhất thu hàng.</p>
<p>Tiếp đó, Tần Giác thanh Linh tộc mỗi một góc đều lật ra một bản, thẳng đến xác
định lại vậy không có vật gì tốt về sau, lúc này mới một quyền đem tất cả kiến
trúc đẩy ngang, chỉ lưu lại một cái to lớn khe rãnh.</p>
<p>... </p>
<p>"Sư phụ làm sao còn chưa có đi ra ." </p>
<p>Vân Tịch ghé vào Tô Ngạn trong tay, ngốc mao không ngừng đong đưa, mặt mũi
tràn đầy lo lắng.</p>
<p>"Yên tâm, tiền bối nhất định không có việc gì ." </p>
<p>Tô Ngạn mở miệng an ủi, cũng coi như là đang an ủi mình.</p>
<p>Hai người bọn họ ở chỗ này chờ hơn nửa ngày thời gian, thủy chung không thấy
Tần Giác đi ra, nội tâm khó tránh khỏi hội có chút khẩn trương.</p>
<p>Nhất là trước đó còn có thể nhìn thấy bên trong sáng lên kim quang, truyền đến
linh lực ba động, nhưng bây giờ lại bình tĩnh vô cùng, chỉ sợ mặc cho ai đều
hội nhịn không được suy nghĩ lung tung.</p>
<p>Đúng lúc này, phía trước bỗng nhiên xuất hiện một bóng người, hướng về bên này
bay tới, ngoại trừ Tần Giác, còn có thể là ai? </p>
<p>"Sư phụ!" </p>
<p>"Tiền bối!" </p>
<p>Vân Tịch cao hứng từ Tô Ngạn trong tay nhảy ra, mong muốn nhào vào Tần Giác
trong ngực, nhưng rất nhanh nàng liền phát hiện chính mình còn không biết bay,
may mà Tô Ngạn phản ứng nhanh, vội vàng tiếp nhận nàng.</p>
<p>Tần Giác xuyên qua bình chướng, đi vào trước mặt hai người, lạnh nhạt nói:
"Tốt, chúng ta đi thôi ." </p>
<p>"A, Linh tộc đâu?" </p>
<p>Tô Ngạn vô ý thức hỏi.</p>
<p>"Linh tộc? Trên cái thế giới này đã không có Linh tộc ." </p>
<p>Tần Giác nhún vai.</p>
<p>Ai có thể nghĩ tới, đứng hàng mười hai Thánh tộc một trong Linh tộc, hội là
đến từ dị giới kẻ xâm lược đâu? </p>
<p>Sở dĩ không có phá hủy bên ngoài cái này lớp bình phong, là bởi vì Tần Giác
tạm thời không muốn để cho ngoại giới biết Linh tộc đã hủy diệt tin tức, nếu
không khẳng định sẽ khiến Trung Châu chấn động, thậm chí dẫn đến cái khác
Thánh tộc vì tranh đoạt tài nguyên, sinh ra tranh đấu.</p>
<p>Tần Giác không muốn nhìn thấy cảnh tượng như thế này, chí ít tại hắn du lịch
trong khoảng thời gian này như thế.</p>
<p>Linh tộc từ trước đến nay quỷ dị, chỉ cần cái này lớp bình phong không có vỡ
vụn, liền không ai dám tuỳ tiện thăm dò, các cái khác Thánh tộc phát giác được
không đối lúc, Tần Giác từ lâu rời đi Trung Châu thánh địa.</p>
<p>"..." </p>
<p>Nghe vậy, Tô Ngạn há to miệng, á khẩu không trả lời được.</p>
<p>Cho tới giờ khắc này, nàng mới chính thức ý thức được Tần Giác cường đại.</p>
<p>Đây chính là mười hai Thánh tộc một trong Linh tộc a! </p>
<p>Cứ như vậy không có? </p>
<p>"Chúng ta về trước Ám Nguyệt thành a ." </p>
<p>Tần Giác không có giải thích, trực tiếp thi triển thuần di thần thông, mang
theo Tô Ngạn cùng Vân Tịch trở lại Ám Nguyệt thành.</p>
<p>Nếu như đã cứu ra Nghiêm Hưu nguyên hồn, như vậy bước kế tiếp liền là nên nghĩ
biện pháp để hắn sống lại.</p>
<p>Dù sao, Tần Giác mới đầu liền chỉ là vì cứu Nghiêm Hưu mà thôi.</p>
<p>"Trực tiếp nhét vào trong miệng hắn là được rồi a?" </p>
<p>Tần Giác cau mày nói.</p>
<p>"Không được, như thế rất dễ dàng dẫn đến nguyên hồn xói mòn, biến thành ngớ
ngẩn ." Tô Ngạn nhắc nhở.</p>
<p>"Ách ... Vậy phải làm thế nào ." </p>
<p>Tần Giác thúc thủ vô sách, hắn còn chưa hề gặp được qua tình huống tương tự.</p>
<p>"Dùng linh lực chậm rãi dung hợp hẳn là có thể ." Tô Ngạn trầm ngâm một lát,
nói ra.</p>
<p>"Dạng này a ." </p>
<p>Tần Giác nhẹ gật đầu, tìm cái tương đối yên lặng địa phương, lấy ra Nghiêm Hưu
nguyên hồn cùng nhục thể, thôi động linh lực, bắt đầu đem cả hai dung hợp.</p>
<p>Bởi vì là lần đầu tiên, Tần Giác có vẻ hơi cẩn thận từng li từng tí, may mà
Nghiêm Hưu nguyên hồn thoát ly nhục thể vẫn chưa tới một ngày, cho dù hơi có
vẻ suy yếu, nhưng ở Tần Giác linh lực tẩm bổ dưới, rất nhanh liền khôi phục
như lúc ban đầu.</p>
<p>Ông.</p>
<p>Nhàn nhạt kim quang bao phủ lại Nghiêm Hưu, hóa thành vô tận linh lực tràn vào
nguyên hồn cùng nhục thể, không biết qua bao lâu, nương theo lấy một tiếng
vang nhỏ, rốt cục dung hợp thành công! </p>
<p>Khi Nghiêm Hưu khi mở mắt ra, kim quang đã tiêu tán, hắn mờ mịt nháy nháy mắt,
một bộ ta là ai, ta ở đâu biểu lộ.</p>
<p>"Kỳ quái, ta không là chết sao?" </p>
<p>Nghiêm Hưu bóp thanh bắp đùi mình, đau nhe răng nhếch miệng.</p>
<p>"Chẳng lẽ ta không chết?" </p>
<p>Nghiêm Hưu có chút mộng bức, hắn nhớ rõ ràng mình giống như bị cái gì đồ vật
hút đi nguyên hồn, về sau liền mất đi ý thức, chẳng lẽ lại là đang nằm mơ? </p>
<p>"Ân? Đây là ... Linh tửu?" </p>
<p>Nghiêm Hưu chợt thấy trước mặt mình để đó một bình linh tửu, chí ít đạt đến
sáu mươi năm trở lên! </p>
<p>"Không đúng, đây không phải ta cái kia ấm linh tửu ." </p>
<p>Nghiêm Hưu trong nháy mắt phân biệt ra được, hắn linh tửu mặc dù cũng đạt tới
sáu mươi năm, nhưng nhưng lại xa xa không cách nào cùng cái này ấm so sánh,
chỉ một thoáng, Nghiêm Hưu nghĩ đến Tần Giác.</p>
<p>"Là hắn đã cứu ta sao?" </p>
<p>"..." </p>
<p>Ám Nguyệt thành trên không, Tần Giác nhẹ nhàng thở ra: "Chúng ta đi thôi ." </p>
<p>Vì cứu một cái biên giới thành nhỏ Địa giai võ giả, cuối cùng tiêu diệt một
cái Thánh tộc, chuyện này nếu để cho ngoại nhân biết, chỉ sợ hội chấn kinh một
vùng ánh mắt a? </p>
<p>(Giấy Trắng: Chúc đạo hữu luôn vui vẻ bên những người đạo hữu yêu quý.)</p>
